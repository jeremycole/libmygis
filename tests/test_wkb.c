int main(int argc, char **argv) {
  WKB *wkb;
  GEOMETRY *geo;
  char *test[6] = { point_1, point_2, linestring_1, polygon_1, NULL };
  int size[6] = { 21, 21, 41, 93, 0 };  
  char **p;
  int *s;
  char *w;
  char *i, *j;
  int x;

  DBUG_ENTER("main");
  DBUG_PROCESS(argv[0]);
  DBUG_PUSH("d:t");

  wkb = wkb_init(0);
  for(p=test, s=size; *p; p++, s++) {
    if(wkb_load(wkb, *p, *s, WKB_F_DUPE) != 0) {
      fprintf(stderr, "Couldn't load WKB.\n");
      exit(1);
    }
    while( (geo = wkb_read_next(wkb)) ) {
      printf("wkb size = %i\n", wkb_size(geo));
      w = wkb_write(geo, NULL);
      if(!memcmp(*p, w, *s)) {
        printf("match!\n");
      } else {
        printf("no match!\n");
        for(i = *p, j = w, x=0; x < *s; i++, j++, x++) {
          printf("  %02i: p = %02x, w = %02x\n", x, *((char *)i), *((char *)j));
        }
      }
      free(w);
      geometry_dump(geo, 5);
      geometry_free(geo);
    };
  };
  wkb_free(wkb);

  DBUG_RETURN(0);
}
